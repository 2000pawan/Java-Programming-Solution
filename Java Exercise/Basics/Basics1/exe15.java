public class exe15 {
    public static void main(String [] arg){
        int a,b,temp;
        a=10;
        b=24;
        System.out.println("Before swapping : a, b = " + a + ", " + b);
        temp=a;
        a=b;
        b=temp;
        System.out.println("After swapping : a, b = " + a + ", " + b);
    }
}
